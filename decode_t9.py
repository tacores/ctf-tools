# 携帯テンキーのマッピング辞書
t9_mapping = {
    '2': 'A', '22': 'B', '222': 'C',
    '3': 'D', '33': 'E', '333': 'F',
    '4': 'G', '44': 'H', '444': 'I',
    '5': 'J', '55': 'K', '555': 'L',
    '6': 'M', '66': 'N', '666': 'O',
    '7': 'P', '77': 'Q', '777': 'R', '7777': 'S',
    '8': 'T', '88': 'U', '888': 'V',
    '9': 'W', '99': 'X', '999': 'Y', '9999': 'Z',
}

# 入力された数字列（スペース区切り）
input_sequence = "55 444 3 6 2 66 7777 7 2 7777 7777 9 666 777 3 444 7777 7777 666 7777 8 777 2 66 4 33"

# 分割して1つずつマッピング
words = input_sequence.strip().split()
decoded = ""

for w in words:
    decoded += t9_mapping.get(w, "?")  # 未知のキーは ? にしておく

print(decoded)
